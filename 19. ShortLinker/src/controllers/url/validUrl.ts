export const validUrl = (url: string) => {
  //(?:[a-z]{2}|[a-z]{3}|[a-z]{4}|[a-z]{5}) ДОМЕН (ЗНАЧЕННЯ ПІСЛЯ КРАПКИ)
  var regURL =
    /^(?:(?:https?|ftp|telnet):\/\/(?:[a-z0-9_-]{1,32}(?::[a-z0-9_-]{1,32})?@)?)?(?:(?:[a-z0-9-]{1,128}\.)+(?:[a-z]{2}|[a-z]{3}|[a-z]{4}|[a-z]{5})|(?!0)(?:(?!0[^.]|255)[0-9]{1,3}\.){3}(?!0|255)[0-9]{1,3})(?:\/[a-z0-9.,_@%&?+=\~\/-]*)?(?:#[^ \'\"&<>]*)?$/i;

  return regURL.test(url);
};
